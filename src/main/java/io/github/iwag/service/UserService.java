package io.github.iwag.service;

import io.github.iwag.domain.Authority;
import io.github.iwag.domain.User;
import io.github.iwag.repository.AuthorityRepository;
import io.github.iwag.repository.PersistentTokenRepository;
import io.github.iwag.config.Constants;
import io.github.iwag.repository.UserRepository;
import io.github.iwag.security.AuthoritiesConstants;
import io.github.iwag.security.SecurityUtils;
import io.github.iwag.service.util.RandomUtil;
import io.github.iwag.service.dto.UserDTO;

import org.slf4j.Logger;
import org.slf4j.LoggerFactory;
import org.springframework.data.domain.Page;
import org.springframework.data.domain.Pageable;
import org.springframework.scheduling.annotation.Scheduled;
import org.springframework.security.authentication.UsernamePasswordAuthenticationToken;
import org.springframework.security.core.GrantedAuthority;
import org.springframework.security.core.authority.SimpleGrantedAuthority;
import org.springframework.security.core.context.SecurityContextHolder;
import org.springframework.security.core.userdetails.UserDetails;
